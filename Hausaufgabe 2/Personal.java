package com.sample;


public class Personal {

  private long pId;
  private String pName;
  private long abt;


  public long getPId() {
    return pId;
  }

  public void setPId(long pId) {
    this.pId = pId;
  }


  public String getPName() {
    return pName;
  }

  public void setPName(String pName) {
    this.pName = pName;
  }


  public long getAbt() {
    return abt;
  }

  public void setAbt(long abt) {
    this.abt = abt;
  }

}
